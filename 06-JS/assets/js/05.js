/* -----------------------------------
    INCREMENTATION & DECREMENTATION
------------------------------------ */

// ## ~ INCREMENTATION ~ ## //

var nb1 = 1;
nb1 = nb1 + 1;
// Ecriture Simplifié
nb1++;

// ## ~ DECREMENTATION ~ ## //

var nb1 = 1;
nb1 = nb1 - 1;
// Ecriture Simplifié
nb1--;

// ## ~ SUBTILITE ~ ## //

nb1 = 0;
console.log(nb1++); // 0
console.log(nb1); // 1

nb1 = 0;
console.log(++nb1); // 1